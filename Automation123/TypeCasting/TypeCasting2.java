package TypeCasting;

public class TypeCasting2 {

	public static void main(String[] args) {

		byte i=10;
		int j=i;
		System.out.println(i);
		System.out.println(j);
		
		
		float a=6555.0f;
		short b=(short)a;
		
		System.out.println(a);
		System.out.println(b);
		
		
		
	}

}
