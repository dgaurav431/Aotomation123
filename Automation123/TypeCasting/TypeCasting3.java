package TypeCasting;

public class TypeCasting3 {

	public static void main(String[] args) {

		int i=11;
		int j=2;
		double result= i/j;
		
		System.out.println(result);
		
		float a=2;
		float b=2.4f;
		byte c=(byte)b;
		b=(byte) b;
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		
		
	}

}
