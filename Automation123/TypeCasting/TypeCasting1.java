package TypeCasting;

public class TypeCasting1 {

	public static void main(String[] args) {

		long a=65540;
		short b=(short)a;
		System.out.println(a);
		System.out.println(b);
		
		double c=456.2545612446;
		float d=(float)c;
		System.out.println(c);
		System.out.println(d);
		
	}

}
