package Program_Practice;

public class Math_Operation {

	public static void main(String[] args) {

		System.out.println(Math.pow(12,4));
		System.out.println(Math.sqrt(144));
		System.out.println(Math.max(12,14));
		System.out.println(Math.min(10,20));
		System.out.println(Math.max(Math.max(38,48),74));
		System.out.println(Math.ceil(43.4));
		System.out.println(Math.floor(45.3));
		System.out.println(Math.abs(14));
		System.out.println(Math.abs(44-50));
		System.out.println(Math.exp(4));
		System.out.println(Math.sin(45));
		System.out.println(Math.sinh(5));
	}

}
