package LoopingStatement;

public class Do_While_Multiplication {

	public static void main(String[] args) {
		int start= 1;
		while(start<=5) {
			System.out.println(start);
			start++;
		}
		System.out.println("Num from 1 to 5");
        System.out.println("---------------------");
        int start2= 1;
        do {
        	System.out.println(start2);
        	start2++;
        }while(start2<=5);
	}

}
