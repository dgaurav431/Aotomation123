package Operators;

public class ArithmeticOp5 {

	public static void main(String[] args) {
		
		int i= 15;
		i= --i;
		i= i++;
		System.out.println(i);
		System.out.println(--i);
		System.out.println(i--);
		
		int j=1;
		int k= j++;
		System.out.println(j);
		System.out.println(k);
		System.out.println(--j);
		System.out.println(--k);
		
	}

}
