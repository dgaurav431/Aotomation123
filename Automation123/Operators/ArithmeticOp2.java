package Operators;

public class ArithmeticOp2 {

	public static void main(String[] args) {

		int i = 2;
		int j = 2;
		i = j+2;
		j= i++;
		 System.out.println(i);
		 System.out.println(j);
		 System.out.println(i++);
		 System.out.println(++i);
		 System.out.println(++j);
		 System.out.println(j++);
		 System.out.println(i++ + j);
		 System.out.println(i++ + ++j);
		
	
	}

}
