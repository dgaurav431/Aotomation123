package Operators;

public class ArithmeticOp1 {

	public static void main(String[] args) {


		System.out.println(10+2);
		System.out.println(10.0+2);
		System.out.println(10.0+2.0);
		
		System.out.println(10*2);
		System.out.println(10.0*2);
		System.out.println(10.0*2.0);
		
		System.out.println(9/2);
		System.out.println(9.0/2);
		System.out.println(9.0/2.0);
		
		System.out.println(11%2);
		System.out.println(11.0%2);
		System.out.println(11.0%2.0);
		
		System.out.println(10-20);
		System.out.println(10.0-20);
		System.out.println(10.0-20.0);
		
	}

}
