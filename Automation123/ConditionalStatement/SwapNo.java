package ConditionalStatement;

public class SwapNo {

	public static void main(String[] args) {
      int num1=35;
      int num2=77;
      System.out.println("-----------------------");
      System.out.println("num1 is "+ num1);
      System.out.println("num2 is "+ num2);
      int i=num1;
      num1=num2;
      num2=i;
      System.out.println("--------------------");
      System.out.println("num1 is " + num1);
      System.out.println("num2 is " + num2);
	}

}
